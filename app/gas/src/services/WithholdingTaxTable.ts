/**
 * 日額表（甲欄・扶養0人）から源泉徴収税額を求める
 * @param dailyAmount その日の社会保険料等控除後の給与等の金額（円）
 * @returns 源泉徴収税額（円、端数切り捨て）
 */
// eslint-disable-next-line @typescript-eslint/no-unused-vars
export function lookupDailyWithholdingTax(dailyAmount: number): number {
  if (dailyAmount <= 0) return 0;

  // 24,000円超: 累進計算式
  if (dailyAmount >= 24000) {
    // 該当する累進区間を逆順で探索
    for (let i = WITHHOLDING_TAX_PROGRESSIVE_R7.length - 1; i >= 0; i--) {
      const [threshold, baseTax, rate] = WITHHOLDING_TAX_PROGRESSIVE_R7[i];
      if (dailyAmount >= threshold) {
        return Math.floor(baseTax + (dailyAmount - threshold) * rate);
      }
    }
  }

  // 23,900円以下: テーブル参照（二分探索）
  const table = WITHHOLDING_TAX_TABLE_R7;
  let lo = 0;
  let hi = table.length - 1;

  while (lo <= hi) {
    const mid = (lo + hi) >>> 1;
    if (table[mid][0] <= dailyAmount) {
      lo = mid + 1;
    } else {
      hi = mid - 1;
    }
  }

  // hi は dailyAmount >= table[hi][0] を満たす最大のインデックス
  return hi >= 0 ? table[hi][1] : 0;
}
